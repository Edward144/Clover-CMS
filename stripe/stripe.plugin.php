        }

        function stripeIsRegistered($customerId) {
            global $mysqli;

            $linkedUsers = $mysqli->query("SELECT stripe_customer_id FROM `users` WHERE stripe_customer_id IS NOT NULL AND stripe_customer_id <> '' ORDER BY first_name, last_name");

            if($linkedUsers->num_rows > 0) {
                $linkedUsers = $linkedUsers->fetch_array();
            }
            else {
                $linkedUsers = [];
            }

            return (in_array($customerId, $linkedUsers));
        }

